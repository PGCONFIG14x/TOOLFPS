# Define rainbow color code
colors=(
    "\033[31m"  # Red
    "\033[33m"  # Yellow
    "\033[32m"  # Green
    "\033[36m"  # Cyan
    "\033[34m"  # Blue
    "\033[35m"  # Magenta
)

reset="\033[0m"

# Function to print rainbow-colored text
rainbow() {
    local text="$1"
    local i
    local text_length=${#text}

    for ((i=0; i<text_length; i++)); do
        color_index=$((i % ${#colors[@]}))
        printf "${colors[$color_index]}${text:$i:1}"
    done
    printf "$reset\n"
}

